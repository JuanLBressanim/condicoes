var diaSemana=parseInt(prompt("Digite um número de 1 a 7"));

if(diaSemana==1){
    console.log("Hoje é Domingo");
}else if(diaSemana==2){
    console.log("Hoje é Segunda-feira");
}else if(diaSemana==3){
    console.log("Hoje é Terça-feira");
}else if(diaSemana==4){
    console.log("Hoje é Quarta-feira");
}else if(diaSemana==5){
    console.log("Hoje é Quinta-feira");
}else if(diaSemana==6){
    console.log("Hoje é Sexta-feira");
}else if(diaSemana==7){
    console.log("Hoje é Sabado");
}else{
    console.log("Número indisponivel, digite novamente");
}